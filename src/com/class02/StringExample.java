package com.class02;

public class StringExample {

	public static void main(String[] args) {
		String name;
		String greeting = "Good morning!";

		name = "Chris";

		System.out.println(name);
		System.out.println(greeting);

		System.out.println("Hello, my name is " + name + ".");

		char grade = 'A';
		System.out.println(name + " is an " + grade + " student!");

		String model = "Lamborgini";
		int year = 2020;

		System.out.println("I drive " + year + " " + model + ".");

		String computer = "Macbook";
		int memory = 2000;

		System.out.println(name + " has a " + computer + " with a " + memory + " GB memory.");

		String example = year + " " + model;

		System.out.println(example);

		int day = 7;
		String month = "February";
		String end = "th";

		String together = month + " " + day + end;
		System.out.println(together);
		
		
		
		

	}

}
